  export const renderShortEmail = (email) => {
    return email?.length >= 9 ? `${email?.slice(0, 8)}...` : email;
  };
  
  export const toCapitalize = (str) => {
    if (str?.length <= 1) {
      return str?.toUpperCase();
    }
    let result = str.split(" ");
    result = result.map((item) => {
      if (item?.length <= 1) {
        return item?.toUpperCase();
      }
      let first = item[0].toUpperCase();
      return first + item.slice(1);
    });
  
    return result.join(" ");
  };
  
  export const renderLongString = (str, len = 7) => {
    str = `${str}`;
    if (str?.length > len) {
      return `${str?.slice(0, len)}...`;
    }
    return str;
  };
  
  export const renderLongFloatString = (str, len = 12) => {
    str = `${str}`;
    let left = str?.split(".")?.[0];
    let right = str?.split(".")?.[1] || "";
    if (left?.length > len) {
      return `${left.slice(0, len)}...`;
    }
    let result = `${left}${right ? "." : ""}${right?.slice(0, 2)}`;
  
    return result?.length > len || right?.length > 2
      ? `${result.slice(0, len)}...`
      : result;
  };
  